class Cipher(object):
    pass

    @staticmethod
    def crypt(key="", data=""):
        print("Chiffrer")
        print("Key : ")
        print(key)
        print("Data : ")
        print(data)
        return "Chiffré !"

    @staticmethod
    def decrypt(key="", data=""):
        print("DÉchiffrer")
        print("Key : ")
        print(key)
        print("Data : ")
        print(data)
        return "Déchiffré"
